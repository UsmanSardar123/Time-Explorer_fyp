import 'package:timeexplorer/features/personalities/domain/entities/character.dart';
import 'package:timeexplorer/features/personalities/domain/entities/character_category.dart';

class CharacterModel extends Character {
  const CharacterModel({
    required super.id,
    required super.name,
    required super.category,
    required super.era,
    required super.description,
    required super.origin,
    super.imageUrl = '',
    super.title = '',
    super.dob = '',
    super.dod = '',
    super.bio = '',
    super.chatPrompt = '',
    super.specialties = const [],
    super.quiz = const [],
    super.contributions = const [],
    super.facts = const [],
    super.contextFacts = const {},
    super.nationality,
    super.achievements,
    super.legacy,
    super.tone,
    super.communicationStyle,
    super.domainKnowledge,
    super.knowledgeCutoffYear,
    super.speechStyle = '',
    super.emotionalTriggers = const [],
    super.fallbackResponses = const [],
    super.rateLimitWarning = '',
    super.civilizationId,
    super.chronologicalOrder,
  });

  factory CharacterModel.fromMap(Map<String, dynamic> map, String id) {
    return CharacterModel(
      id: id,
      name: map['name'] ?? '',
      category: CharacterCategory.fromString(map['category'] ?? 'scientists'),
      era: map['era'] ?? '',
      description: map['description'] ?? '',
      imageUrl: map['imageUrl'] ?? '',
      title: map['title'] ?? '',
      dob: map['dob'] ?? map['birthYear'] ?? '',
      dod: map['dod'] ?? map['deathYear'] ?? '',
      bio: map['bio'] ?? '',
      origin: map['origin'] ?? map['nationality'] ?? '',
      chatPrompt: map['chatPrompt'] ?? '',
      tone: map['tone'],
      communicationStyle: map['communicationStyle'],
      domainKnowledge: map['domainKnowledge'],
      specialties: (map['specialties'] as List?)?.map((e) => e.toString()).toList() ?? [],
      quiz: (map['quiz'] as List?)?.map((e) {
        final qMap = Map<String, dynamic>.from(e as Map);
        return QuizQuestion(
          question: qMap['question'] ?? '',
          options: List<String>.from(qMap['options'] ?? []),
          correctIndex: qMap['correctIndex'] ?? 0,
          explanation: qMap['explanation'] ?? '',
        );
      }).toList() ?? [],
      contributions: (map['contributions'] as List?)?.map((e) => e.toString()).toList() ?? [],
      facts: (map['facts'] as List?)?.map((e) => e.toString()).toList() ?? [],
      contextFacts: (map['contextFacts'] as Map<String, dynamic>?)?.map(
            (key, value) => MapEntry(key, value.toString()),
          ) ??
          {},
      nationality: map['nationality'] ?? map['origin'],
      achievements: (map['achievements'] as List?)?.map((e) => e.toString()).toList(),
      legacy: map['legacy'],
      knowledgeCutoffYear: map['knowledgeCutoffYear'] as int?,
      speechStyle: map['speechStyle'] ?? '',
      emotionalTriggers: (map['emotionalTriggers'] as List?)?.map((e) => e.toString()).toList() ?? [],
      fallbackResponses: (map['fallbackResponses'] as List?)?.map((e) => e.toString()).toList() ?? [],
      rateLimitWarning: map['rateLimitWarning'] ?? '',
      civilizationId: map['civilizationId'] as String?,
      chronologicalOrder: map['chronologicalOrder'] as int?,
    );
  }

  Map<String, dynamic> toMap() => {
    'name': name,
    'category': category.name,
    'era': era,
    'description': description,
    'imageUrl': imageUrl,
    'title': title,
    'dob': dob,
    'dod': dod,
    'origin': origin,
    'nationality': nationality,
    'achievements': achievements,
    'legacy': legacy,
    'bio': bio,
    'chatPrompt': chatPrompt,
    'tone': tone,
    'communicationStyle': communicationStyle,
    'domainKnowledge': domainKnowledge,
    'specialties': specialties,
    'quiz': quiz.map((q) => {
      'question': q.question,
      'options': q.options,
      'correctIndex': q.correctIndex,
      'explanation': q.explanation,
    }).toList(),
    'contributions': contributions,
    'facts': facts,
    'contextFacts': contextFacts,
    'knowledgeCutoffYear': knowledgeCutoffYear,
    'speechStyle': speechStyle,
    'emotionalTriggers': emotionalTriggers,
    'fallbackResponses': fallbackResponses,
    'rateLimitWarning': rateLimitWarning,
    'civilizationId': civilizationId,
    'chronologicalOrder': chronologicalOrder,
  };
}
